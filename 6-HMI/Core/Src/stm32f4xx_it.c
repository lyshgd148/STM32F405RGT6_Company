void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  if (htim == &htim2)
  {
    size = queue_find_cmd(cmd_buffer, CMD_MAX_SIZE);
    if (size > 0 && cmd_buffer[1] != 0x07)
    {
      ProcessMessage((PCTRL_MSG)cmd_buffer, size);
    }
  }
}

