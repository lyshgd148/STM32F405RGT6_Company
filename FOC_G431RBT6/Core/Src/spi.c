uint8_t KTH7823_tx_data[2] = {0, 0};
uint8_t KTH7823_rx_data[2];
uint8_t num = 0;

void HAL_SPI_TxRxCpltCallback(SPI_HandleTypeDef *hspi)
{
  if (hspi->Instance == SPI1)
  {
    num++;
    num %= 2;
    static float encode_angle_last = 0;
    static int once = 1;
    HAL_GPIO_WritePin(SPI1_CS_GPIO_Port, SPI1_CS_Pin, GPIO_PIN_SET);
    if (num == 0)
    {
      int angle_raw = (uint16_t)(KTH7823_rx_data[0] << 8) | (uint16_t)(KTH7823_rx_data[1]);
      encoder_angle = 2.0 * angle_raw / (1 << 16) * PI;
      if (once)
      {
        once = 0;
        encode_angle_last = encoder_angle;
      }
      float diff_angle = cycle_diff(encoder_angle - encode_angle_last, 2 * PI);
      encode_angle_last = encoder_angle;
      motor_logic_angle = cycle_diff(motor_logic_angle + diff_angle, position_cycle);
    }

    HAL_GPIO_WritePin(SPI1_CS_GPIO_Port, SPI1_CS_Pin, GPIO_PIN_RESET);
    HAL_SPI_TransmitReceive_DMA(&hspi1, KTH7823_tx_data, KTH7823_rx_data, 2);
    // return;
  }
}

